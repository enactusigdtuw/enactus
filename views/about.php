				<h1 style="color:#ff0000;">#Our Story</h1>
                    <p style="color:#663300;">Bringing together the passionate and driven people of our campus, we started our chapter of ENACTUS over two years ago with the goal to look for opportunities to improve the livelihood of people in need by making them self sufficient and self reliant.<br>On 16th October 2015, we launched our first project: Khidmat. Initially Khidmat was a tiffin service where we helped underprivileged women to set up their own businesses by utilising their cooking skills. We have branched out to catering service and food stalls to target a larger demographic and provide opportunities to more women in need.<br>To tackle the problem of increasing use of plastic, we started our second project in July 2016: REAP. By devising a tool which shreds plastic bottles of different shapes into thin long strips, Project REAP aims to reduce the production of plastic and recycle the existing bottles to minimize the damage done to the environment. These strips of plastic are modified and woven together to design a range of commercial products. To expand this project, we are planning to create a set of machineries which would shred, mould and transform plastic bottles into sheets which can be used to make viable products.<br>In July 2016, Enactus IGDTUW participated in the Enactus Nationals, held in Mumbai and emerged victorious in the Rookie League, becoming the first all girls team ever to win the competition. It was a proud moment for all the members who represented the team in Nationals and for all the other members as well, when the team stood on stage and lifted the trophy which substantiated all the hard work.<br>Since then, we have worked to expand these projects, targeting new customers and creating new machineries. We are dedicated towards connecting more and more people with our organisation so as to create sustainable employment opportunities for them and to help them become entrepreneurs.
