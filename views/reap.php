                <div class="intro-paragraph">
				<h1 style="color:#ffff00;">#Business Model</h1>
                    <p>Our target audience comprises of the informal-pickers. Through field visits and research, we found out that the informal-pickers get paid less as compared to the actual rates of the same plastic bottles sold off to the factories.<br>
We are targeting the environment issues of plastic waste.We researched and worked upon possible development of tools and machines to form products which can be pitched back in the market as commercialized products. </p>
<p style="margin:10px;color:#ffcc00;">Our model involves creating a shredder which converts plastic bottles into smaller bits and pieces. The beneficiaries themselves collect the plastic bottles, shred and transport them. Thus, the removal of the middle men helps generate extra income for the ragpickers.</p><p>
Moreover, the maintenance of financial records and transaction details by the workers provide them with a technical mind and an entrepreneurial skill set.
</p>
